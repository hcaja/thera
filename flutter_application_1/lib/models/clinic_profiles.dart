class Employee {
  int id;
  String email;
  String password;
  String username;
  String name;
  String role;
  int clinicAccount;
  String address;
  String contactNo;
  String age;
  String sex;
  String profilePicture;

  Employee({
    required this.id,
    required this.email,
    required this.password,
    required this.username,
    required this.name,
    required this.role,
    required this.clinicAccount,
    required this.address,
    required this.contactNo,
    required this.age,
    required this.sex,
    required this.profilePicture,
  });
}
